package patterns.behavioral.command.housecontrol.light;

import patterns.behavioral.command.housecontrol.Command;

public class LightOnCommand implements Command {
    Light light;

    public LightOnCommand(Light light) {
        this.light = light;
    }

    public void execute() {
        light.on();
    }

    public void undo() {
        light.off();
    }
}