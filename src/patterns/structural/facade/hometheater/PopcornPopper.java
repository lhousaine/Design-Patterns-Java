package patterns.structural.facade.hometheater;

public class PopcornPopper {

    public void on() {
        System.out.println("The popcornPopper was turned On");
    }

    public void pop() {
        System.out.println("The popcornPopper was poped");
    }

    public void off() {
        System.out.println("The popcornPopper was turned Off");
    }
}
